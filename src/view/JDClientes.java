    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        // TODO add your handling code here:
       if(!jTextFieldNome.getText().equals("")){
           int opcao = JOptionPane.showConfirmDialog(null,"Tem certeza que deja exlucir?");
           if(opcao == JOptionPane.YES_OPTION){
              if(c.excluir(Integer.parseInt(jTextFieldID.getText())) == true){
              JOptionPane.showMessageDialog(null, "Cliente ExcluÃ­do");
              clientes = c.retornar();
              preencherJTable(clientes);
              limparcampos();
           }
           
           }
       }else{
           JOptionPane.showMessageDialog(null, "Selecione o cliente a ser excluÃ­do.");
       }
    }//GEN-LAST:event_jButtonExcluirActionPerformed

    private void jTextFieldPesquisarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldPesquisarKeyPressed
        // TODO add your handling code here:
         if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            if(jRadioButtonPnome.isSelected()){
            clientes = c.pesquisa(jTextFieldPesquisar.getText(), "nome");
            }else if(jRadioButtonPcpf.isSelected()){
            clientes = c.pesquisa(jTextFieldPesquisar.getText(), "cpf");
            }else if(jRadioButtonPrg.isSelected()){
            clientes = c.pesquisa(jTextFieldPesquisar.getText(), "rg");    
            }            
            preencherJTable(clientes);
        }
        
    }//GEN-LAST:event_jTextFieldPesquisarKeyPressed

