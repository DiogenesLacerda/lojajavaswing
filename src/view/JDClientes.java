    public void preencherJTable(List<Cliente> clientes){
        DefaultTableModel modelo = (DefaultTableModel) jTableLista.getModel();
        modelo.setNumRows(0);
        
        for(Cliente p : clientes){
            modelo.addRow(new Object[]{
               p.getId(),
               p.getNome(),
               p.getCpf(),
               p.getRg(),
               p.getRua(),
               p.getNumero(),
               p.getBairro(),
               p.getCep(),
               p.getTelefone(),                         
               p.getSexo()
            });
            
        }
    }

