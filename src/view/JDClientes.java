            jTextFieldNum.setText(jTableLista.getValueAt(jTableLista.getSelectedRow(), 5).toString());
            jTextFieldBairro.setText(jTableLista.getValueAt(jTableLista.getSelectedRow(), 6).toString());
            jTextFieldCep.setText(jTableLista.getValueAt(jTableLista.getSelectedRow(), 7).toString());
            jTextFieldFone.setText(jTableLista.getValueAt(jTableLista.getSelectedRow(), 8).toString());
